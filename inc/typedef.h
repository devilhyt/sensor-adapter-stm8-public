#ifndef TYPEDEF_H
#define TYPEDEF_H

#include <stdbool.h>
#include <stdint.h>

/* Basic data types */
typedef uint8_t u8;   //, UBYTE;
typedef uint16_t u16; //, UWORD;
typedef uint32_t u32; //, ULONG;
typedef int8_t s8;    //, SBYTE, DATA8;
typedef int16_t s16;  //, SWORD, DATA16;
typedef int32_t s32;  //, SLONG, DATA32;
typedef float f32;    //, FLOAT, DATAF;

#endif /* TYPEDEF_H */